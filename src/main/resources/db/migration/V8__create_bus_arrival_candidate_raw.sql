create table bus_arrival_candidate_raw
(
    id                     bigint generated by default as identity
        primary key,
    created_at             timestamp(6),
    deleted_at             timestamp(6),
    updated_at             timestamp(6),
    collected_at           timestamp(6) not null,
    finalized_at           timestamp(6) not null,
    first_seen_at          timestamp(6),
    last_seen_at           timestamp(6),
    last_collected_at      timestamp(6),
    route_id               varchar(255),
    route_abrv             varchar(255),
    route_name             varchar(255),
    stop_id                varchar(255),
    ars_id                 varchar(255),
    stop_name              varchar(255),
    seq                    integer,
    direction              varchar(255),
    route_type             integer,
    term                   integer,
    data_timestamp         varchar(255),
    detour_yn              varchar(255),
    next_bus_yn            varchar(255),
    first_bus_time         varchar(255),
    last_bus_time          varchar(255),
    arrival_order          integer,
    arrival_msg            varchar(255),
    vehicle_id             varchar(255),
    plain_no               varchar(255),
    vehicle_identity       varchar(255),
    vehicle_identity_type  varchar(255),
    bus_type               integer,
    section_order          integer,
    station_name           varchar(255),
    is_arrive              varchar(255),
    is_last                varchar(255),
    is_full                varchar(255),
    predict_time           integer,
    kal_predict_time       integer,
    neu_predict_time       integer,
    goal_time              integer,
    avg_coefficient        double precision,
    exp_coefficient        double precision,
    kal_coefficient        double precision,
    neu_coefficient        double precision,
    section_time           integer,
    section_speed          double precision,
    congestion_num         integer,
    congestion_div         integer,
    ride_num               integer,
    ride_div               integer,
    next_stop_id           varchar(255),
    next_stop_ord          integer,
    next_stop_sec          integer,
    next_stop_spd          integer,
    main_stop_ord          integer,
    main_stop_sec          integer,
    main_stop_id           varchar(255),
    main2_stop_ord         integer,
    main2_stop_sec         integer,
    main2_stop_id          varchar(255),
    main3_stop_ord         integer,
    main3_stop_sec         integer,
    main3_stop_id          varchar(255)
);

create index idx_bus_arrival_candidate_raw_scope_finalized
    on bus_arrival_candidate_raw (route_id, stop_id, seq, finalized_at);

create index idx_bus_arrival_candidate_raw_vehicle
    on bus_arrival_candidate_raw (route_id, stop_id, seq, vehicle_identity_type, vehicle_identity);
